#  Write a Python program to add elements to a list using insert() and append().

lst_number = [1,2,3,4,5,6]
print (lst_number)

# insert element in lst:
lst_number.insert(7,20)
print (lst_number)

# insert element using append :
lst_number.append("hello")
print (lst_number)

#  Write a Python program to remove elements from a list using pop() and remove().

lst_number.pop()
print (lst_number)

# delete element using remove :

lst_number.remove(6)
print (lst_number)